#include <sys/time.h>
#include <stdio.h>
#include <stdlib.h>
#include <time.h>
#include <unistd.h>

int			main(int argc, char **argv)
{
     struct timeval time; 
    gettimeofday(&time,NULL);	 
	int i;
	int k;
	int j;
	int a;
	int ii;
	int r;
	int		g_tetro[19][4][2]={{{0,0},{0,1},{1,0},{2,0}},
		{{0,0},{0,1},{1,1},{2,1}},
		{{0,0},{1,0},{2,0},{2,1}},
		{{0,1},{1,1},{2,0},{2,1}},
		{{0,0},{0,1},{0,2},{1,2}},
		{{0,0},{0,1},{0,2},{1,0}},
		{{0,0},{1,0},{1,1},{1,2}},
		{{0,2},{1,0},{1,1},{1,2}},
		{{0,0},{0,1},{1,0},{1,1}},
		{{0,1},{1,0},{1,1},{2,1}},
		{{0,0},{1,0},{1,1},{2,0}},
		{{0,1},{1,0},{1,1},{1,2}},
		{{0,0},{0,1},{0,2},{1,1}},
		{{0,0},{1,0},{1,1},{2,1}},
		{{0,0},{0,1},{1,1},{1,2}},
		{{0,1},{1,0},{1,1},{2,0}},
		{{0,1},{0,2},{1,0},{1,1}},
		{{0,0},{0,1},{0,2},{0,3}},
		{{0,0},{1,0},{2,0},{3,0}},
	};
	srand((time.tv_sec * 100) + (time.tv_usec / 100));
	argc--;
	argv++;
	k = -1;
	if (argc !=1 || atoi(argv[0]) < 1 )
	{
		printf("merci d'entrer un nombre de tetrosi\n");
		exit(1);
	}
	while(++k < atoi(argv[0]))
	{

		r = rand() % 19;
		i=-1;
		while(++i<4)
		{
			j=-1;
			while(++j	<4)
			{
				ii=-1;
				a=0;
				while(++ii<4)
				{
					if(g_tetro[r][ii][0]==i && g_tetro[r][ii][1]==j)
						a=1;
				}
				if(a)
					write(1,"#",1);
				else
					write(1,".",1);
			}
			write(1,"\n",1);
		}
		if (k + 1 != atoi(argv[0])) 
			write(1,"\n",1);
	}
}
