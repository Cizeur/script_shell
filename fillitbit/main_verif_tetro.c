#include <stdio.h>
#include <stdlib.h>
int			main(int argc, char **argv)
{
	int i;
	int k;
	int j;
	int a;
	int ii;
int		g_tetro[19][4][2]={{{0,0},{0,1},{1,0},{2,0}},
	{{0,0},{0,1},{1,1},{2,1}},
	{{0,0},{1,0},{2,0},{2,1}},
	{{0,1},{1,1},{2,0},{2,1}},
	{{0,0},{0,1},{0,2},{1,2}},
	{{0,0},{0,1},{0,2},{1,0}},
	{{0,0},{1,0},{1,1},{1,2}},
	{{0,2},{1,0},{1,1},{1,2}},
	{{0,0},{0,1},{1,0},{1,1}},
	{{0,1},{1,0},{1,1},{2,1}},
	{{0,0},{1,0},{1,1},{2,0}},
	{{0,1},{1,0},{1,1},{1,2}},
	{{0,0},{0,1},{0,2},{1,1}},
	{{0,0},{1,0},{1,1},{2,1}},
	{{0,0},{0,1},{1,1},{1,2}},
	{{0,1},{1,0},{1,1},{2,0}},
	{{0,1},{0,2},{1,0},{1,1}},
	{{0,0},{0,1},{0,2},{0,3}},
	{{0,0},{1,0},{2,0},{3,0}},
};
	system("clear all");
	printf("\n       Bienvenue dans le monde magique des tetrominos       \n\n");
	printf("\nEntrez nombre entre 0 et 18 en argument pour isoler un tetro\n\n");
	argc--;
	argv++;
	k = -1;
	while(++k<19)
	{
		if (argc && atoi(argv[0]) >= 0 && atoi(argv[0]) < 19)
		{
			if(atoi(argv[0]) != k)
				continue;
		}
		printf("\033[32mTetrominos:%i\n",k);
		i=-1;
		while(++i<4)
		{
			j=-1;
			while(++j	<4)
			{
				ii=-1;
				a=0;
				while(++ii<4)
				{
					if(g_tetro[k][ii][0]==i && g_tetro[k][ii][1]==j)
						a=1;
				}
				if(a)
					printf("\033[36m░░ ");
				else
					printf("\033[37m───");
			}
			printf("\n");
		}
		printf("\n");
	}
}
